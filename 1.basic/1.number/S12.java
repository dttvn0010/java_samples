// Tìm 2 số biết tổng và hiệu
public class S12 {
    public static void main(String[] args) {
        int S = 10; // tổng
        int D = 4; // hiệu

        int a = (S+D)/2;
        int b = (S-D)/2;

        System.out.println("Số lớn: " + a);
        System.out.println("Số bé: " + b);      
    }
}

